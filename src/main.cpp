// Forward declaration
void drawProgressBar(int x, int y, int width, int height, int progress);

// Fungsi untuk reset progress bar state
void resetProgressBar()
{
  // Force static variables di drawProgressBar untuk reset
  drawProgressBar(0, 0, 0, 0, -1);
}

// Fungsi untuk menggambar progress bar
void drawProgressBar(int x, int y, int width, int height, int progress)
{
  // Static variables untuk tracking state terakhir
  static int lastProgress = -1;
  static int lastX = -1;
  static int lastDisplayedProgress = -1;
  
  // Reset mode: clear static variables
  if (progress == -1)
  {
    lastProgress = -1;
    lastX = -1;
    lastDisplayedProgress = -1;
    return;
  }
  
  // progress: 0-100%
  
  // Hanya gambar border & background SEKALI (first time atau posisi berubah)
  if (lastProgress == -1 || lastX != x)
  {
    // Outer border (square)
    tft.drawRect(x, y, width, height, TFT_WHITE);
    
    // Fill background
    tft.fillRect(x + 2, y + 2, width - 4, height - 4, TFT_BLACK);
    
    lastX = x;
  }
  
  // Hitung fill width
  int fillWidth = ((width - 4) * progress) / 100;
  int lastFillWidth = lastProgress >= 0 ? ((width - 4) * lastProgress) / 100 : 0;
  
  // Hanya update bagian yang berubah (incremental update)
  if (fillWidth > lastFillWidth)
  {
    uint16_t color;
    if (progress < 33)
      color = TFT_RED;
    else if (progress < 66)
      color = TFT_YELLOW;
    else
      color = TFT_GREEN;
    
    // Gambar HANYA bagian baru yang bertambah
    int updateX = x + 2 + lastFillWidth;
    int updateWidth = fillWidth - lastFillWidth;
    
    if (updateWidth > 0)
    {
      tft.fillRect(updateX, y + 2, updateWidth, height - 4, color);
    }
  }
  
  lastProgress = progress;
  
  // Update text HANYA setiap 5% untuk mengurangi flicker
  if (progress - lastDisplayedProgress >= 5 || progress == 100 || progress == 0)
  {
    // Clear area text saja
    int textX = x + width / 2 - 30;
    int textY = y + height / 2 - 8;
    tft.fillRect(textX, textY, 60, 16, TFT_BLACK);
    
    // Draw text
    tft.setTextColor(TFT_WHITE);
    tft.setTextDatum(MC_DATUM);
    tft.setTextSize(2);
    tft.drawString(String(progress) + "%", x + width / 2, y + height / 2);
    
    lastDisplayedProgress = progress;
  }
}

